/*

DOM - Document Object Model = It is a programming interface for HTML and XML documents. It represents the page so that programs
                              can change the document structure, style, and content. The DOM represents the document as nodes and 
                              objects. That way, programming languages can connect to the page.
                                
                              The DOM is an object-oriented representation of the web page, which can be modified with a scripting language like JavaScript.

                              The DOM is a W3C (World Wide Web Consortium) standard.

                              The DOM defines the logical structure of documents and the way a document is accessed and manipulated.
*/

console.log(document); // It will show the whole document object model of the page.
